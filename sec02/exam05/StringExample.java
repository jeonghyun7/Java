package sec02.exam05;

public class StringExample {
	public static void main(String[] args) {
		String name = "홍길동";
		String job = "프로그래머";
		
		System.out.println(name);
		System.out.println(job);
		
		char a = '홍';
		char b = '길';			// char타입으로 String과 같이 이름출력
		char c = '동';
		
		System.out.print(a);
		System.out.print(b);
		System.out.print(c);
	}
}